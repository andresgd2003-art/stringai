        // Custom smooth scroll and navbar close
        const navLinks = document.querySelectorAll('#navbarNav .nav-link');
        const toggler = document.querySelector('.navbar-toggler');
        const menuCollapse = document.getElementById('navbarNav');
        const navbar = document.querySelector('.navbar');

        navLinks.forEach(link => {
            link.addEventListener('click', function(e) {
                // 1. Prevent default anchor behavior
                e.preventDefault();

                // 2. Get target ID
                const targetId = this.getAttribute('href');
                const targetElement = document.querySelector(targetId);

                if (targetElement) {
                    // 3. Calculate navbar height
                    const navbarHeight = navbar.offsetHeight;

                    // 4. Calculate position to scroll to
                    const targetPosition = targetElement.getBoundingClientRect().top + window.pageYOffset - navbarHeight;

                    // 5. Scroll to the position
                    window.scrollTo({
                        top: targetPosition,
                        behavior: 'smooth'
                    });

                    // 6. Close the mobile menu if it's open
                    if (toggler.offsetParent !== null && menuCollapse.classList.contains('show')) {
                        toggler.click();
                    }
                }
            });
        });